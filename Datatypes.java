public class Datatypes
{
    public static void main(String [] args)
    {
        int a =10;
        float b =10.4f;
        double c= 20000d;
        char ch = 'A';
        String st ="hello world";
        boolean bool = true;
        short s =100;
        long lo = 100_00_00_00_000l;
        byte by = -128;
        System.out.println("numeric datatype--->"+ " integer = "+ a +" float = "+ b + " double = "+ c +" short = "+ s + " long = "+lo+" byte = "+ by);  
        System.out.println("alphabets datatype --->"+ " char = "+ch+" string = "+st);
        System.out.println("logical datatype --->"+" boolean --->"+bool);
    }
}
